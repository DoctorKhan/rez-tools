 
% Hungarian Notation - Variable Prefixes
%
% Prefix        Meaning
% ------        -------
% v             vector
% m             matrix (2d)
% ts            time series (3d)
% t             3-d matrix (same as time series)
% t4d           4-d matrix
% tNd           N-d matrix
% z             complex (z = x + iy)
% b             logical (boolean)
% s             string
% p             string (pathname)
% c             cell
% o             structure (object)
% i             integer (or index)
% iv            vector of indices of a vector element
% n             whole number (or total number)
% h             handle
% f             floating point single precision
% none          floating point double
% u8            unsigned 8-bit integer
% i8            signed 8-bit integer
% x             unknown data type
%
% Examples      Meaning
% --------      -------
% vbAnswer      logical vector
% tzData        complex time series
% oView         structure
% csText        cell structure of strings
% cOther        cell structure
%
% This is the variable naming convention used to make the code easier to
% interpret and help in reducing errors.
%
